    def _add_risk_assessment(self, doc, results):
        """Tambah risk assessment"""
        doc.add_heading('âš ï¸ RISK ASSESSMENT', level=1)
        
        # Count risks by level
        risk_counts = {'High': 0, 'Medium': 0, 'Low': 0}
        for item in results['non_compliant_items']:
            risk_level = item.get('risk_level', 'Medium')
            risk_counts[risk_level] += 1
        
        # Risk summary table
        risk_table = doc.add_table(rows=4, cols=3)
        risk_table.style = 'Medium Grid 1 Accent 1'
        
        # Headers
        risk_table.cell(0, 0).text = 'Risk Level'
        risk_table.cell(0, 1).text = 'Count'
        risk_table.cell(0, 2).text = 'Priority'
        
        for i in range(3):
            risk_table.cell(0, i).paragraphs[0].runs[0].font.bold = True
        
        # Data
        risk_data = [
            ('ðŸ”´ High Risk', risk_counts['High'], 'Immediate Action Required'),
            ('ðŸŸ¡ Medium Risk', risk_counts['Medium'], 'Address Within 30 Days'),
            ('ðŸŸ¢ Low Risk', risk_counts['Low'], 'Monitor and Improve')
        ]
        
        for i, (level, count, priority) in enumerate(risk_data, 1):
            risk_table.cell(i, 0).text = level
            risk_table.cell(i, 1).text = str(count)
            risk_table.cell(i, 2).text = priority
        
        doc.add_paragraph()
        
        # Risk mitigation priorities
        doc.add_paragraph("ðŸŽ¯ PRIORITAS MITIGASI RISIKO:")
        
        priorities = []
        if risk_counts['High'] > 0:
            priorities.append(f"â€¢ URGENT: {risk_counts['High']} high-risk items memerlukan tindakan segera")
        if risk_counts['Medium'] > 0:
            priorities.append(f"â€¢ PENTING: {risk_counts['Medium']} medium-risk items perlu ditangani dalam 30 hari")
        if risk_counts['Low'] > 0:
            priorities.append(f"â€¢ MONITOR: {risk_counts['Low']} low-risk items untuk perbaikan berkelanjutan")
        
        for priority in priorities:
            doc.add_paragraph(priority)
    
